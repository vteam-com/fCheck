// This file demonstrates various comment styles
// It should have a high comment ratio

/*
 * Multi-line comment at the top
 * This is another line
 * And another
 */

/// This is a documentation comment for the class
class CommentsExample {
  // Regular single-line comment

  /// Documentation comment for a method
  void documentedMethod() {
    // Comment inside method
    print('Hello'); // Inline comment
  }

  /*
   * Multi-line comment
   * inside the class
   */
  void anotherMethod() {
    // Another comment
    /* Block comment */
    print('World');
  }

  // Final comment
}
